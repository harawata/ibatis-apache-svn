        sb.append(cd.getAliasedColumnName());
        sb.append(' ');
        sb.append(operator);
        sb.append("\", value, \""); //$NON-NLS-1$
        sb.append(cd.getJavaProperty());
        sb.append("\");"); //$NON-NLS-1$
